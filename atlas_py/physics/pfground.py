def pfground(nelion: int, t: float) -> float:
    """Ground-state partition function for non-iron-group elements.

    Replicates ``FUNCTION PFGROUND(NELION, T)`` in atlas12.for (line 22116).

    Parameters
    ----------
    nelion : int
        Combined element-ionization index: ``(IZ-1)*6 + ION`` for most
        elements.  Iron-group indices 115–168 are not handled here (use
        ``pfiron`` instead); this function returns 1.0 for those.
    t : float
        Temperature in Kelvin.

    Returns
    -------
    float
        Ground-state statistical weight (≥ 1.0).
    """
    entry = _PFGROUND_DATA.get(nelion)
    if entry is None:
        return 1.0
    base, terms = entry
    if not terms:
        return base
    inv_t = _HCK / max(t, 1.0)
    result = base
    for g, e in terms:
        result += g * math.exp(-inv_t * e)
    return result


def pfiron(nelem: int, ion: int, tlog10: float, potlow_cm1: float) -> float:
    """Iron-group partition function from the precomputed PFTAB table.

    Replicates ``SUBROUTINE PFIRON(NELEM, ION, TLOG8, POTLOW8, PF)`` in
    atlas12.for (line 16070).

    Parameters
    ----------
    nelem : int
        Atomic number, 20 (Ca) through 28 (Ni).
    ion : int
        Ionization stage (1 = neutral, 2 = singly ionized, …).  Up to 10.
    tlog10 : float
        log₁₀(T).  The Fortran caller passes ``TLOG(J)/2.30258…`` where
        TLOG is the natural log; equivalent to ``log10(T)``.
    potlow_cm1 : float
        Debye potential-lowering in cm⁻¹.  The Fortran caller passes
        ``POTLO(ION) * 8065.479``.

    Returns
    -------
    float
        Partition function value (linear scale, not log).
    """
    if not (20 <= nelem <= 28):
        raise ValueError(f"pfiron: nelem={nelem} outside valid range 20–28")
    if not (1 <= ion <= 10):
        raise ValueError(f"pfiron: ion={ion} outside valid range 1–10")

    pftab = _get_pftab()  # shape (7, 56, 10, 9), 0-based Python indices

    tlog = float(tlog10)

    # Determine temperature bin IT (1-based, Fortran convention) and
    # fractional position F within the interval.
    if tlog > 4.0:
        it = int((tlog - 4.0) / 0.05) + 31
        it = min(it, 56)
        f = (tlog - (it - 31) * 0.05 - 4.0) / 0.05
    elif tlog < 3.7:
        it = int((tlog - 3.32) / 0.02) + 2
        it = max(it, 2)
        f = (tlog - (it - 2) * 0.02 - 3.32) / 0.02
    else:
        it = int((tlog - 3.7) / 0.03) + 21
        f = (tlog - (it - 21) * 0.03 - 3.7) / 0.03

    # Convert to 0-based Python indices
    it0 = it - 1          # upper temperature bin (0-based)
    it0m1 = it0 - 1       # lower temperature bin (0-based)
    ion0 = ion - 1        # ionization stage (0-based)
    elem0 = nelem - 20    # element offset (0-based: Ca=0, Sc=1, …, Ni=8)

    # Find Debye-level bracket: LOW is the upper bracket (1-based Fortran).
    potlow = float(potlow_cm1)
    low = 1
    if potlow < _PFIRON_POTLO[0]:
        # Below lowest level: use lowest bin directly.
        pf = (
            f * pftab[low - 1, it0, ion0, elem0]
            + (1.0 - f) * pftab[low - 1, it0m1, ion0, elem0]
        )
        return float(pf)

    for low in range(2, 8):
        if potlow < _PFIRON_POTLO[low - 1]:
            # potlow is between POTLO[low-2] and POTLO[low-1]: interpolate.
            p = (math.log10(potlow) - _PFIRON_POTLOLOG[low - 2]) / 0.30103
            pf = (
                p * (f * pftab[low - 1, it0, ion0, elem0]
                     + (1.0 - f) * pftab[low - 1, it0m1, ion0, elem0])
                + (1.0 - p) * (f * pftab[low - 2, it0, ion0, elem0]
                                + (1.0 - f) * pftab[low - 2, it0m1, ion0, elem0])
            )
            return float(pf)

    # Above highest level: use highest bin directly.
    low = 7
    pf = (
        f * pftab[low - 1, it0, ion0, elem0]
        + (1.0 - f) * pftab[low - 1, it0m1, ion0, elem0]
    )
    return float(pf)
