# ──────────────────────────────────────────────────────────────────────────────
# PFIRON constants (atlas12.for PFIRON subroutine)
# ──────────────────────────────────────────────────────────────────────────────
# POTLO(1..7): Debye potential-lowering levels in cm^-1
_PFIRON_POTLO: tuple[float, ...] = (500.0, 1000.0, 2000.0, 4000.0, 8000.0, 16000.0, 32000.0)
# POTLOLOG(1..7): log10 of the above
_PFIRON_POTLOLOG: tuple[float, ...] = (
    2.69897, 3.0, 3.30103, 3.60206, 3.90309, 4.20412, 4.50515,
)
# Temperature grid parameters:
#   bins  2..20 : TLOG step 0.02 starting at 3.32 (T ≈ 2089 – 10000 K)
#   bins 21..30 : TLOG step 0.03 starting at 3.70 (T ≈ 5012 – 10000 K)
#   bins 31..56 : TLOG step 0.05 starting at 4.00 (T ≈ 10000 – 178000 K)

# Lazy-loaded PFTAB cache
_PFTAB_CACHE: np.ndarray | None = None


def _get_pftab() -> np.ndarray:
    global _PFTAB_CACHE
    if _PFTAB_CACHE is None:
        _PFTAB_CACHE = load_pfiron_tables()["PFTAB"]
    return _PFTAB_CACHE


